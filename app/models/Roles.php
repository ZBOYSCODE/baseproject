<?php
namespace Gabs\Models;

class Roles extends \Phalcon\Mvc\Model
{

    /**
     *
     * @var integer
     */
    public $rol_id;

    /**
     *
     * @var string
     */
    public $description;

    /**
     * Initialize method for model.
     */
